package com.amolotkoff.mocker.parser.model.params;

public class Param {
    private String name;
    private String value;

    public Param(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return name;
    }
}
